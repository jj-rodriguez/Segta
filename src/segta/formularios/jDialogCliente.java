    //Convierte la fila seleccionada en objeto cliente
    @SuppressWarnings("empty-statement")
    private void seleccionaCliente() {
        if (this.jTableClientes.getSelectedRowCount() == 1) {
            cli = clienteList.get(jTableClientes.getSelectedRow());
            this.jTextRazonSocial.setText(cli.getRazonSocial());

            try {
                this.jTextCUIT.setText(cli.getCuit().toString());
            } catch (Exception ex) {
                this.jTextCUIT.setText("");
            };

            try {
                this.jTextDireccion.setText(cli.getDireccion());
            } catch (Exception ex) {
                this.jTextDireccion.setText("");
            };

            try {
                this.jTextMail.setText(cli.getMail());
            } catch (Exception ex) {
                this.jTextMail.setText("");
            };
            try {
                this.jTextTelefono.setText(cli.getTelefono().toString());
            } catch (Exception ex) {
                this.jTextTelefono.setText("");
            };
            try {
                this.jTextContacto.setText(cli.getContacto());
            } catch (Exception ex) {
                this.jTextContacto.setText("");
            };
            try {
                this.jTextPais.setText(cli.getPais());
            } catch (Exception ex) {
                this.jTextPais.setText("");
            };
        }
    }

    //Limpia los textFields
    private void limpiaCampos() {
        this.jTextRazonSocial.setText("");
        this.jTextCUIT.setText("");
        this.jTextDireccion.setText("");
        this.jTextMail.setText("");
        this.jTextTelefono.setText("");
        this.jTextPais.setText("");
        this.jTextContacto.setText("");
    }

    //desactiva los textfields y botones de aceptar y cancelar
    private void desactivaCampos() {
        for (Component component : jPanelDatos.getComponents()) {
            component.setEnabled(false);
        }

    }

    //activa los textfields y botones de aceptar y cancelar
    private void activaCampos() {
        for (Component component : jPanelDatos.getComponents()) {
            component.setEnabled(true);
        }

    }

    //desactiva botones de agregar y editar y tabla 
    private void desactivaTabla() {
        for (Component component : jPanelTabla.getComponents()) {
            component.setEnabled(false);
        }
        this.jTableClientes.setEnabled(false);

    }
    //activa botones de agregar y editar y tabla 

    private void activaTabla() {
        for (Component component : jPanelTabla.getComponents()) {
            component.setEnabled(true);
        }
        this.jTableClientes.setEnabled(true);

    }

    private void actualizaTabla() {
        clienteList.clear();
        clienteList.addAll(clienteQuery.getResultList());
    }

