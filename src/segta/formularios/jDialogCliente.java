    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        int flagValidacion = 0;
        String razSoc = this.jTextRazonSocial.getText();
        BigInteger cuit = null;
        BigInteger tel = null;

        //Razon social no null
        if (razSoc.isEmpty()) {
            flagValidacion = 1;
            JOptionPane.showMessageDialog(null, "Ingrese una Razón Social", "Validación", JOptionPane.WARNING_MESSAGE);
            this.jTextRazonSocial.requestFocus();
        }
        //cuit 
//        cuit = new this.jTextCUIT.getText();

        //telefono numerico
        if (!this.jTextTelefono.getText().isEmpty()) {

            try {
                tel = new BigInteger(this.jTextTelefono.getText());

            } catch (Exception ex) {
                flagValidacion = 1;
                JOptionPane.showMessageDialog(null, "Ingrese sólo números", "Información", JOptionPane.WARNING_MESSAGE);
                this.jTextTelefono.requestFocus();

            }
        }

        if (flagValidacion == 0) {

            if (tipo.equals("nuevo")) {
                Cliente nuevoCli = new Cliente();
                nuevoCli.setRazonSocial(razSoc);
                nuevoCli.setCuit(jTextCUIT.getText());
                nuevoCli.setDireccion(this.jTextDireccion.getText());
                nuevoCli.setTelefono(tel);
                nuevoCli.setMail(this.jTextMail.getText());
                nuevoCli.setContacto(this.jTextContacto.getText());
                nuevoCli.setPais(this.jTextPais.getText());

                try {

                    controlador.create(nuevoCli);
                    JOptionPane.showMessageDialog(null, "Cliente creado con éxito", "Información", JOptionPane.INFORMATION_MESSAGE);
                    limpiaCampos();

                } catch (Exception ex) {
                    Logger.getLogger(jDialogCliente.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Error al crear el cliente", "Información", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                cli.setRazonSocial(razSoc);
                cli.setCuit(jTextCUIT.getText());
                cli.setDireccion(this.jTextDireccion.getText());
                cli.setTelefono(tel);
                cli.setMail(this.jTextMail.getText());
                cli.setContacto(this.jTextContacto.getText());
                cli.setPais(this.jTextPais.getText());
                try {

                    controlador.edit(cli);
                    JOptionPane.showMessageDialog(null, "Cliente editado con éxito", "Información", JOptionPane.INFORMATION_MESSAGE);
                    limpiaCampos();

                } catch (Exception ex) {
                    Logger.getLogger(jDialogCliente.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Error al editar el cliente", "Información", JOptionPane.ERROR_MESSAGE);
                }

            }

            limpiaCampos();
            desactivaCampos();
            activaTabla();
            actualizaTabla();

        }

    }//GEN-LAST:event_jBAceptarActionPerformed
